package revision;

public class BitwiseOperators {
    public static void main(String[] args) {
        System.out.println(4 & 5);
        System.out.println(4 | 5);
        System.out.println(4 ^ 5);
        System.out.println(4 << 1);
        System.out.println(4 >> 1);
        System.out.println(~5);

    }
}
