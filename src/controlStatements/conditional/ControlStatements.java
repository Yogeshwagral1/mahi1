package controlStatements.conditional;

public class ControlStatements {
    public static void main(String[] args) {

        //execute this
        if(true)
        {
            System.out.println("pdf");
            System.out.println("pdf");
            System.out.println("pdf");
            System.out.println("pdf");
        }

        //skip this
        if(false)
        {
            System.out.println("excel");
            System.out.println("excel");
            System.out.println("excel");
            System.out.println("excel");
            System.out.println("excel");

            System.out.println("text");
            System.out.println("text");
            System.out.println("text");
            System.out.println("text");
            System.out.println("text");
        }
    }
}
